var genTitle = function () {
    var pickedVals = [];
    var output = "";
    var sp = " ";
    var if1Ar = ["Een", "Deze"];
    var num = Math.ceil(Math.random() * 50);

    if (num === 1 || num >= 25) {
        num = rand(if1Ar);
    }

    for (var i = 0; i < texts.length; i++) {
        pickedVals.push(rand(texts[i]));
    }

    if (num === "A" && pickedVals[0].substring(0, 1).match(/[aeiouAEIOU]/)) {
        num = "An";
    }

    output += num + sp;
    for (var i = 0; i < pickedVals.length; i++) {
        output += sp + pickedVals[i];
        if ((num >= 1 && num <= 25) && i === 0) {
            output += "en ";
        }
        if (i === 2) {
            output += ". ";
        }
    }
    outputText(output);
}


var outputText = function (out) {
    document.getElementById("title-display").innerHTML = out;
}

var rand = function (ar) {
    return ar[Math.floor(Math.random() * ar.length)];
}

